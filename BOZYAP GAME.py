import tkinter as tk
from tkinter import ttk, filedialog, messagebox
from PIL import Image
import pillow_heif
import os
import math
import pygame
import random
import tempfile

def split_image(image_path, piece_count):
    # Open the image file
    if image_path.lower().endswith('.heic') or image_path.lower().endswith('.heif'):
        heif_file = pillow_heif.read_heif(image_path)
        image = Image.frombytes(
            heif_file.mode,
            heif_file.size,
            heif_file.data,
            "raw",
            heif_file.mode,
            heif_file.stride,
        )
    else:
        image = Image.open(image_path)

    image_width, image_height = image.size

    # Calculate the number of rows and columns based on the number of pieces
    cols = int(math.ceil(math.sqrt(piece_count)))
    rows = int(math.ceil(piece_count / cols))

    # Piece dimensions
    piece_width = image_width // cols
    piece_height = image_height // rows

    # Create a temporary folder
    temp_dir = tempfile.mkdtemp()

    # Create pieces
    piece_paths = []
    piece_number = 0
    for row in range(rows):
        for col in range(cols):
            left = col * piece_width
            upper = row * piece_height
            right = (col + 1) * piece_width
            lower = (row + 1) * piece_height

            piece = image.crop((left, upper, right, lower))
            piece_path = os.path.join(temp_dir, f"piece_{piece_number}.png")
            piece.save(piece_path)
            piece_paths.append(piece_path)
            piece_number += 1

    # Fill in missing pieces
    total_pieces = rows * cols
    for i in range(piece_number, total_pieces):
        piece_paths.append(piece_paths[-1])  # Fill by copying the last piece

    return piece_paths, cols, rows

def run_puzzle(piece_paths, cols, rows):
    # Start Pygame
    pygame.init()

    # Screen dimensions
    WIDTH, HEIGHT = 1200, 800
    TILE_WIDTH = WIDTH // cols
    TILE_HEIGHT = HEIGHT // rows

    # Colors
    WHITE = (255, 255, 255)
    GRID_COLOR = (200, 200, 200)
    HIGHLIGHT_COLOR = (0, 255, 0)

    # Create the screen
    screen = pygame.display.set_mode((WIDTH, HEIGHT))
    pygame.display.set_caption("Puzzle Game")

    # Load and scale the pieces
    pieces = [pygame.image.load(piece_path) for piece_path in piece_paths]
    pieces = [pygame.transform.scale(piece, (TILE_WIDTH, TILE_HEIGHT)) for piece in pieces]

    # Correct order of pieces
    correct_order = list(range(len(pieces)))

    # Shuffle the pieces and their order
    shuffled_indices = list(range(len(pieces)))
    random.shuffle(shuffled_indices)
    pieces = [pieces[i] for i in shuffled_indices]

    # Select a random piece
    current_piece_index = 0
    current_piece = pieces[current_piece_index]
    current_x, current_y = (WIDTH // 2 - TILE_WIDTH // 2) // TILE_WIDTH * TILE_WIDTH, (HEIGHT // 2 - TILE_HEIGHT // 2) // TILE_HEIGHT * TILE_HEIGHT

    # Helper function to check positions
    def get_grid_position(x, y):
        col = x // TILE_WIDTH
        row = y // TILE_HEIGHT
        return row * cols + col

    # Function to draw the grid
    def draw_grid():
        for row in range(rows):
            for col in range(cols):
                rect = pygame.Rect(col * TILE_WIDTH, row * TILE_HEIGHT, TILE_WIDTH, TILE_HEIGHT)
                pygame.draw.rect(screen, GRID_COLOR, rect, 1)

    # List to hold the positions of correctly placed pieces
    placed_pieces = [None] * len(pieces)

    # Function to draw text
    def draw_text(screen, text, size, color, x, y):
        font = pygame.font.Font(None, size)
        text_surface = font.render(text, True, color)
        text_rect = text_surface.get_rect()
        text_rect.midtop = (x, y)
        screen.blit(text_surface, text_rect)

    # Function to run the game
    def run_game():
        nonlocal current_piece_index, current_piece, current_x, current_y
        running = True
        game_completed = False
        while running:
            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    running = False
                elif event.type == pygame.KEYDOWN:
                    mods = pygame.key.get_mods()
                    if event.key == pygame.K_LEFT and current_x - TILE_WIDTH >= 0:
                        current_x -= TILE_WIDTH
                    elif event.key == pygame.K_RIGHT and current_x + TILE_WIDTH < WIDTH:
                        current_x += TILE_WIDTH
                    elif event.key == pygame.K_UP and current_y - TILE_HEIGHT >= 0:
                        current_y -= TILE_HEIGHT
                    elif event.key == pygame.K_DOWN and current_y + TILE_HEIGHT < HEIGHT:
                        current_y += TILE_HEIGHT
                    elif event.key == pygame.K_RETURN and not (mods & pygame.KMOD_CTRL) and not (mods & pygame.KMOD_SHIFT):
                        # Check if the piece is in the correct position
                        grid_position = get_grid_position(current_x, current_y)
                        if grid_position == correct_order[shuffled_indices[current_piece_index]]:
                            placed_pieces[grid_position] = current_piece
                            current_piece_index += 1
                            if current_piece_index < len(pieces):
                                current_piece = pieces[current_piece_index]
                                current_x, current_y = (WIDTH // 2 - TILE_WIDTH // 2) // TILE_WIDTH * TILE_WIDTH, (HEIGHT // 2 - TILE_HEIGHT // 2) // TILE_HEIGHT * TILE_HEIGHT
                            else:
                                print("You completed the game!")
                                game_completed = True
                    elif event.key == pygame.K_RETURN and (mods & pygame.KMOD_CTRL) and (mods & pygame.KMOD_SHIFT):
                        # Ctrl + Shift + Enter combination
                        grid_position = correct_order[shuffled_indices[current_piece_index]]
                        row = grid_position // cols
                        col = grid_position % cols
                        placed_pieces[grid_position] = current_piece
                        current_piece_index += 1
                        if current_piece_index < len(pieces):
                            current_piece = pieces[current_piece_index]
                            current_x, current_y = (WIDTH // 2 - TILE_WIDTH // 2) // TILE_WIDTH * TILE_WIDTH, (HEIGHT // 2 - TILE_HEIGHT // 2) // TILE_HEIGHT * TILE_HEIGHT
                        else:
                            print("You completed the game!")
                            game_completed = True

            screen.fill(WHITE)
            draw_grid()

            # Draw the correctly placed pieces
            for index, piece in enumerate(placed_pieces):
                if piece is not None:
                    row = index // cols
                    col = index % cols
                    screen.blit(piece, (col * TILE_WIDTH, row * TILE_HEIGHT))

            # Draw the current piece and its outline
            if current_piece_index < len(pieces):
                screen.blit(current_piece, (current_x, current_y))
                pygame.draw.rect(screen, HIGHLIGHT_COLOR, (current_x, current_y, TILE_WIDTH, TILE_HEIGHT), 2)

            # Show the message when the game is completed
            if game_completed:
                draw_text(screen, "You completed the game!", 80, (255, 0, 0), WIDTH // 2, HEIGHT // 2 + 50)

            pygame.display.flip()

    # Start the game
    run_game()
    pygame.quit()

def main():
    # Create the Tkinter interface
    root = tk.Tk()
    root.title("Puzzle Creator")
    root.geometry("400x300")
    root.configure(bg="#f0f0f0")

    style = ttk.Style()
    style.configure('TButton', font=('Helvetica', 12), padding=10)
    style.configure('TLabel', font=('Helvetica', 12), padding=10)
    style.configure('TEntry', font=('Helvetica', 12), padding=10)
    
    def select_image():
        # Ask the user to select an image file
        image_path = filedialog.askopenfilename(
            title="Select an image file",
            filetypes=[("Image Files", "*.jpg;*.jpeg;*.png;*.heic;*.heif;*.bmp;*.tiff")]
        )
        if image_path:
            image_path_label.config(text=f"Selected file: {image_path}")
            root.image_path = image_path
        else:
            image_path_label.config(text="No image file selected.")

    def start_puzzle():
        if not hasattr(root, 'image_path'):
            messagebox.showerror("Error", "No image file selected.")
            return

        # Ask the user to enter the number of pieces
        piece_count = piece_count_entry.get()
        if not piece_count.isdigit():
            messagebox.showerror("Error", "Please enter a valid number of pieces.")
            return
        piece_count = int(piece_count)
        if piece_count < 2:
            messagebox.showerror("Error", "The number of pieces must be at least 2.")
            return

        # Split the image and start the puzzle
        piece_paths, cols, rows = split_image(root.image_path, piece_count)
        run_puzzle(piece_paths, cols, rows)

    # Information text for selecting the image
    info_label = tk.Label(root, text="Please select an image file for the puzzle:")
    info_label.pack(pady=10)

    # Button to select the image file
    select_image_button = tk.Button(root, text="Select Image", command=select_image)
    select_image_button.pack(pady=5)

    # Label to show the selected file path
    image_path_label = tk.Label(root, text="No image file selected.")
    image_path_label.pack(pady=5)

    # Information text for entering the number of pieces
    piece_count_info_label = tk.Label(root, text="Please enter the number of pieces for the puzzle:")
    piece_count_info_label.pack(pady=10)

    # Entry to enter the number of pieces
    piece_count_entry = tk.Entry(root)
    piece_count_entry.pack(pady=5)

    # Button to start the puzzle
    start_puzzle_button = tk.Button(root, text="Start Puzzle", command=start_puzzle)
    start_puzzle_button.pack(pady=20)

    root.mainloop()

if __name__ == "__main__":
    main()
